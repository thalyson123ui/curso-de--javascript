// operadores bitwise em JavaScript
console.log(5 & 3);  // AND bit a bit
console.log(5 | 3);  // OR bit a bit
console.log(5 ^ 3);  // XOR bit a bit
console.log(~5);     // NOT bit a bit
console.log(5 << 1); // deslocamento à esquerda
console.log(5 >> 1); // deslocamento à direita
console.log(5 >>> 1); // deslocamento à direita sem sinal