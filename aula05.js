// operadores relacionais
console.log(10 > 5);  // maior que
console.log(10 < 5);  // menor que
console.log(10 >= 5); // maior ou igual a
console.log(10 <= 5); // menor ou igual a
console.log(10 == 5); // igual a
console.log(10 != 5); // diferente de

// operadores lógicos
console.log(true && false); // E lógico
console.log(true || false); // OU lógico
console.log(!true);         // NÃO lógico

// operadores de atribuição
let x = 10;        // atribuição simples
x += 5;            // adição e atribuição
x -= 3;            // subtração e atribuição
x *= 2;            // multiplicação e atribuição
x /= 4;            // divisão e atribuição

console.log(x);

// operadores de incremento e decremento
let y = 5;
console.log(y++); // pós-incremento
console.log(++y); // pré-incremento
console.log(y--); // pós-decremento
console.log(--y); // pré-decremento