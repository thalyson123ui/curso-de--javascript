// diferença entre pré-incremento e pós-incremento
let a = 1;
let b = 1;

console.log(++a); // pré-incremento: incrementa antes de usar o valor (resultado: 2)
console.log(b++); // pós-incremento: usa o valor antes de incrementar (resultado: 1)
console.log(b);   // agora b foi incrementado (resultado: 2)